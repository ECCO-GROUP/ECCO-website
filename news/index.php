<h4>November 2006 — Elephant seals serving ECCO</h4>

<p> A unique set of roughly 320,000 individual sub-surface measurements of salinity (conductivity), temperature, and  depth (CTD) taken by elephant seals which carried bio-logging and telemetry devices was added to the ECCO state estimation system  as new observational constraints. The data were kindly provided by the  <a href="http://biology.st-andrews.ac.uk/seaos/index.html" target="_blank">&quot;Southern Elephant Seals as Oceanographic Samplers&quot; (SEaOS)</a> project involving
